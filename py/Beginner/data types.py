# Data Types in Python
# --------------------
# Python has several built-in data types:
# - int: Integer numbers
# - float: Decimal numbers
# - str: Text
# - bool: True or False
# - list: Ordered, mutable collection
# - tuple: Ordered, immutable collection
# - dict: Key-value pairs
# - set: Unordered, unique items

# Examples:
my_int = 10
my_float = 3.14
my_str = "Hello"
my_bool = True
my_list = [1, 2, 3]
my_tuple = (1, 2, 3)
my_dict = {"a": 1, "b": 2}
my_set = {1, 2, 3}

print(type(my_int), type(my_float), type(my_str), type(my_bool), type(my_list), type(my_tuple), type(my_dict), type(my_set))

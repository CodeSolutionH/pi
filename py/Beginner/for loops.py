# For Loops in Python
# -------------------
# A for loop is used to iterate over a sequence (like a list, tuple, or string).

# Example with a list:
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

# Example with a string:
for char in "hello":
    print(char)

# Using range():
for i in range(3):
    print(i)  # Output: 0, 1, 2

# Numbers in Python
# -----------------
# Python has several numeric types: int, float, and complex.

# Integers (int): Whole numbers
x = 10
print(type(x))  # Output: <class 'int'>

# Floating point numbers (float): Numbers with decimals
pi = 3.14159
print(type(pi))  # Output: <class 'float'>

# Complex numbers (complex): Numbers with a real and imaginary part
y = 2 + 3j
print(type(y))  # Output: <class 'complex'>

# You can perform arithmetic operations with numbers:
a = 5
b = 2
print(a + b)  # 7
print(a - b)  # 3
print(a * b)  # 10
print(a / b)  # 2.5

# Type conversion:
print(float(a))  # 5.0
print(int(pi))   # 3
